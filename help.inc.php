<p>
Der Veranstaltungskalender soll eine einfache Verwaltung für Events ermöglichen, die sowohl einmalig, wie widerkehrend sein können. Zudem soll es möglich sein Veranstaltungsorte und die Veranstalter selbst eintragen zu können. Dies ermöglicht eine optimale Durchsuchbarkeit.
<br>
<br>
Für die Zukunft sind auch folgende Features geplant:<br>
- Backup<br>
- In-/Export (csv/json)<br>
- Structured Data<br>
